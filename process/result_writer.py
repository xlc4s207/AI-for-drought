# -*- coding: utf-8 -*-
"""
结果写入模块
生成GeoTIFF频率图和NetCDF事件详情
"""

import os
import numpy as np
import netCDF4 as nc
from osgeo import gdal, osr
from config import (
    RESULT_DIR, YEARS, LAT_SIZE, LON_SIZE, 
    RESOLUTION, LAT_RANGE, LON_RANGE, MAX_EVENTS_PER_PIXEL
)


def create_geotiff(data, filename, nodata=-9999):
    """
    创建GeoTIFF文件
    
    Parameters:
    -----------
    data : numpy.ndarray
        2D数组(lat, lon)
    filename : str
        输出文件名(不含路径)
    nodata : float
        无效值
    """
    filepath = os.path.join(RESULT_DIR, filename)
    
    rows, cols = data.shape
    
    # 创建驱动
    driver = gdal.GetDriverByName('GTiff')
    out_ds = driver.Create(filepath, cols, rows, 1, gdal.GDT_Float32)
    
    # 设置地理变换 (left, pixel_width, rotation, top, rotation, -pixel_height)
    # GLEAM数据是从北到南排列
    geotransform = (LON_RANGE[0], RESOLUTION, 0, LAT_RANGE[1], 0, -RESOLUTION)
    out_ds.SetGeoTransform(geotransform)
    
    # 设置投影(WGS84)
    srs = osr.SpatialReference()
    srs.ImportFromEPSG(4326)
    out_ds.SetProjection(srs.ExportToWkt())
    
    # 写入数据
    out_band = out_ds.GetRasterBand(1)
    out_band.SetNoDataValue(nodata)
    
    # 替换NaN为nodata
    data_out = np.where(np.isnan(data), nodata, data)
    out_band.WriteArray(data_out)
    
    out_band.FlushCache()
    out_ds = None
    
    print(f"[结果写入] GeoTIFF已保存: {filepath}")
    return filepath


def save_total_frequency(frequency_array):
    """
    保存总频率GeoTIFF
    
    Parameters:
    -----------
    frequency_array : numpy.ndarray
        总频率数组(lat, lon)
    """
    filename = f"flash_drought_frequency_total_{YEARS[0]}_{YEARS[-1]}.tif"
    return create_geotiff(frequency_array, filename)


def save_yearly_frequency(yearly_arrays):
    """
    保存各年度频率GeoTIFF
    
    Parameters:
    -----------
    yearly_arrays : dict
        {year: frequency_array, ...}
    """
    filepaths = []
    for year, arr in yearly_arrays.items():
        filename = f"flash_drought_frequency_{year}.tif"
        fp = create_geotiff(arr, filename)
        filepaths.append(fp)
    return filepaths


def create_events_netcdf(lat_array, lon_array):
    """
    创建事件详情NetCDF文件
    
    Returns:
    --------
    filepath : str
        NetCDF文件路径
    """
    filepath = os.path.join(RESULT_DIR, "flash_drought_events_details.nc")
    
    # 创建NetCDF文件
    ds = nc.Dataset(filepath, 'w', format='NETCDF4')
    
    # 创建维度
    ds.createDimension('lat', len(lat_array))
    ds.createDimension('lon', len(lon_array))
    ds.createDimension('max_events', MAX_EVENTS_PER_PIXEL)
    
    # 创建坐标变量
    lat_var = ds.createVariable('lat', 'f4', ('lat',))
    lat_var[:] = lat_array
    lat_var.units = 'degrees_north'
    lat_var.long_name = 'latitude'
    
    lon_var = ds.createVariable('lon', 'f4', ('lon',))
    lon_var[:] = lon_array
    lon_var.units = 'degrees_east'
    lon_var.long_name = 'longitude'
    
    # 创建事件变量
    event_count = ds.createVariable('event_count', 'i2', ('lat', 'lon'), 
                                     fill_value=-1, zlib=True)
    event_count.long_name = 'Total number of flash drought events per pixel'
    
    event_start_year = ds.createVariable('event_start_year', 'i2', 
                                          ('max_events', 'lat', 'lon'),
                                          fill_value=-1, zlib=True)
    event_start_year.long_name = 'Event start year'
    
    event_start_doy = ds.createVariable('event_start_doy', 'i2',
                                         ('max_events', 'lat', 'lon'),
                                         fill_value=-1, zlib=True)
    event_start_doy.long_name = 'Event start day of year'
    
    event_duration = ds.createVariable('event_duration', 'i2',
                                        ('max_events', 'lat', 'lon'),
                                        fill_value=-1, zlib=True)
    event_duration.long_name = 'Event duration in days'
    
    event_intensity = ds.createVariable('event_intensity', 'f4',
                                         ('max_events', 'lat', 'lon'),
                                         fill_value=-9999.0, zlib=True)
    event_intensity.long_name = 'Event intensity (cumulative deficit)'
    
    # 全局属性
    ds.title = 'Flash Drought Events Details'
    ds.source = 'GLEAM SMrz data (1980-2024)'
    ds.institution = 'Generated by flash_drought detection algorithm'
    
    ds.close()
    print(f"[结果写入] NetCDF事件详情文件已创建: {filepath}")
    return filepath


def update_events_netcdf(lat_idx, lon_idx, events, nc_filepath=None):
    """
    更新单个像元的事件数据到NetCDF
    
    Parameters:
    -----------
    lat_idx : int
        纬度索引
    lon_idx : int
        经度索引
    events : list
        事件列表
    nc_filepath : str
        NetCDF文件路径
    """
    if nc_filepath is None:
        nc_filepath = os.path.join(RESULT_DIR, "flash_drought_events_details.nc")
    
    with nc.Dataset(nc_filepath, 'r+') as ds:
        n_events = min(len(events), MAX_EVENTS_PER_PIXEL)
        ds.variables['event_count'][lat_idx, lon_idx] = n_events
        
        for i, event in enumerate(events[:n_events]):
            ds.variables['event_start_year'][i, lat_idx, lon_idx] = event['year']
            ds.variables['event_start_doy'][i, lat_idx, lon_idx] = event['start_doy']
            ds.variables['event_duration'][i, lat_idx, lon_idx] = event['duration']
            ds.variables['event_intensity'][i, lat_idx, lon_idx] = event['intensity']


class ResultCollector:
    """
    结果收集器 - 在内存中累积结果，批量写入
    """
    
    def __init__(self, lat_size, lon_size, lat_array, lon_array):
        self.lat_size = lat_size
        self.lon_size = lon_size
        self.lat_array = lat_array
        self.lon_array = lon_array
        
        # 总频率数组
        self.total_freq = np.full((lat_size, lon_size), np.nan, dtype=np.float32)
        
        # 年度频率数组
        self.yearly_freq = {
            year: np.full((lat_size, lon_size), np.nan, dtype=np.float32)
            for year in YEARS
        }
        
        # 事件详情NC文件路径
        self.nc_filepath = None
        
        # 统计计数
        self.processed_count = 0
        self.valid_count = 0
        self.total_events = 0
    
    def initialize_nc(self):
        """初始化事件详情NC文件"""
        self.nc_filepath = create_events_netcdf(self.lat_array, self.lon_array)
    
    def add_pixel_result(self, lat_idx, lon_idx, events, yearly_counts):
        """
        添加单个像元的结果
        """
        total_count = len(events)
        
        # 更新总频率
        self.total_freq[lat_idx, lon_idx] = total_count
        
        # 更新年度频率
        for year, count in yearly_counts.items():
            self.yearly_freq[year][lat_idx, lon_idx] = count
        
        # 更新事件详情
        if events:
            update_events_netcdf(lat_idx, lon_idx, events, self.nc_filepath)
        
        # 更新统计
        self.processed_count += 1
        self.valid_count += 1
        self.total_events += total_count
    
    def mark_invalid(self, lat_idx, lon_idx):
        """标记无效像元"""
        self.total_freq[lat_idx, lon_idx] = np.nan
        for year in YEARS:
            self.yearly_freq[year][lat_idx, lon_idx] = np.nan
        self.processed_count += 1
    
    def save_all(self):
        """保存所有结果"""
        print("\n" + "="*60)
        print("[保存结果]")
        print("="*60)
        
        # 保存总频率
        save_total_frequency(self.total_freq)
        
        # 保存年度频率
        save_yearly_frequency(self.yearly_freq)
        
        print(f"\n[统计] 处理像元数: {self.processed_count}")
        print(f"[统计] 有效像元数: {self.valid_count}")
        print(f"[统计] 检测到骤旱事件总数: {self.total_events}")
    
    def print_progress(self, current, total):
        """打印进度"""
        percent = current / total * 100
        print(f"\r[进度] {current}/{total} ({percent:.1f}%) | "
              f"有效像元: {self.valid_count} | 事件总数: {self.total_events}", 
              end='', flush=True)
